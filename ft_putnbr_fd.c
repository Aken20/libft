#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*s;
	int		i;

	s = ft_itoa(n);
	i = 0;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	free (s);
}
